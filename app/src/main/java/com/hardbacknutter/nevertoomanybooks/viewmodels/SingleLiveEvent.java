package com.hardbacknutter.nevertoomanybooks.viewmodels;

import androidx.annotation.MainThread;
import androidx.annotation.NonNull;
import androidx.annotation.Nullable;
import androidx.lifecycle.LifecycleOwner;
import androidx.lifecycle.MutableLiveData;
import androidx.lifecycle.Observer;

import java.util.concurrent.atomic.AtomicBoolean;

/**
 * A lifecycle-aware observable that sends only new updates after subscription,
 * <p>
 * This avoids a common problem with events: on configuration change (like rotation) an update
 * can be emitted if the observer is active. This LiveData only calls the observable if there's an
 * explicit call to setValue().
 * <p>
 * <strong>Note that only one observer is going to be notified of changes.</strong>
 *
 * <a href="https://github.com/android/architecture-samples/tree/dev-todo-mvvm-live">
 * https://github.com/android/architecture-samples/tree/dev-todo-mvvm-live</a>
 * <p>
 * See {@link LiveDataEvent} for an alternative.
 */
public class SingleLiveEvent<T>
        extends MutableLiveData<T> {

    private final AtomicBoolean mPending = new AtomicBoolean(false);

    @MainThread
    public void observe(@NonNull final LifecycleOwner owner,
                        @NonNull final Observer<? super T> observer) {

        if (hasActiveObservers()) {
            throw new IllegalStateException("Multiple observers registered");
        }

        // Observe the internal MutableLiveData
        super.observe(owner, t -> {
            if (mPending.compareAndSet(true, false)) {
                observer.onChanged(t);
            }
        });
    }

    @MainThread
    public void setValue(@Nullable final T t) {
        mPending.set(true);
        super.setValue(t);
    }
}
